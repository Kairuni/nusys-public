local function determinePossibleShapeAffs(st)
    local possibleShapeAffs = {};
    local allPossibleHiddenAffs = {};
    for _,hiddenAff in ipairs(st.hidden) do
        for aff, state in pairs(hiddenAff.affMap) do
            if (state == true) then
                allPossibleHiddenAffs[aff] = true;
            end
        end
    end

    for shape, affList in pairs(AFFS.shapeAffOrder) do
        for _, aff in ipairs(affList) do
            if (not st.affs[aff]) then
                table.insert(possibleShapeAffs, aff);
            end
            if (not st.affs[aff] and not allPossibleHiddenAffs[aff]) then
                break;
            end
        end
    end
    return possibleShapeAffs;
end

